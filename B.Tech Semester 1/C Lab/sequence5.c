#include <stdio.h>

int main()
{
    printf("Newline (\\n): \"\n\"\n");
    printf("Tab (\\t): \"\t\"\n");
    printf("Backspace (\\b): \"\b\"\n");
    printf("Formfeed (\\f): \"\f\"\n");
    printf("Carriage Return (\\r): \"\r\"\n");
    printf("Escape (\\e): \"\e\"\n");
    printf("Bell (\\a): \"\a\"\n");
}